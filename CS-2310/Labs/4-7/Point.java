// Write a class called Point that has two int fields x and y coordinates.
// The class should have the ncecessary accessors, mutators. and constructors.

import java.util.*;


public class Point {

     int x;
     int y;

    public Point () {
        x = x;
        y = y;
    }

    public Point (int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int i) {
        x = i;;
    }

    public void setY(int j) {
         y = j;
    }
    public void setCoor(int f , int g) {
        x = f;
        y = g;
    }
}
